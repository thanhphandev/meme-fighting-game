/**
 * Get a random dialogue for a character and event
 */
export function getDialogue(characterId, event) {
    const charDialogues = CHARACTER_DIALOGUES[characterId];
    if (!charDialogues) {
        // Fallback to generic
        return getGenericDialogue(event);
    }

    const dialogues = charDialogues[event];
    if (!dialogues || dialogues.length === 0) {
        return getGenericDialogue(event);
    }

    return dialogues[Math.floor(Math.random() * dialogues.length)];
}

/**
 * Generic dialogues for unknown characters
 */
function getGenericDialogue(event) {
    const generic = {
        intro: ["Let's fight!", "Ready!", "Chiến thôi!"],
        attack: ["Take this!", "Đây!", "Hit!"],
        skill: ["SPECIAL!", "Chiêu đặc biệt!", "ULTIMATE!"],
        hit: ["Ow!", "Đau!", "Oof!"],
        block: ["Blocked!", "No!", "Chặn!"],
        win: ["Victory!", "Winner!", "Thắng rồi!"],
        lose: ["Nooo!", "GG...", "Thua rồi..."],
        taunt: ["Come on!", "Weak!", "Yếu lắm!"],
    };

    const dialogues = generic[event] || ["..."];
    return dialogues[Math.floor(Math.random() * dialogues.length)];
}

